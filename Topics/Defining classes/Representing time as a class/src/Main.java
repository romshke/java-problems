class Time {
    final int hours;
    final int minutes;
    final int seconds;

    Time() {
        this.hours = 0;
        this.minutes = 0;
        this.seconds = 0;
    }

    Time(int hours, int minutes, int seconds) {
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }
}